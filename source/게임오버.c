int gameover(void)
{
	CursorView(0);

	PlaySound(TEXT("resource/게임오버.wav"), NULL, SND_ASYNC | SND_LOOP);

	
	while (true) {
		gameover_print();
		Score();
		int Menu_Code = Menu();


		
		 if (Menu_Code == 1)
		{
			system("cls");
			start();
		}
		else if (Menu_Code == 2)
		{
			exit(100); //게임종료
		}
		system("cls");
	}

	return 0;
}

