int Menu()      //메뉴 출력
{
	char c;
	int x = 65;
	int y = 42;
	gotoxy(x - 2, y);
	printf("> 다 시 시 작");
	gotoxy(x, y + 1);
	printf("게 임 종 료");

	while (1) {   //무한루프
		int n = keyControl();
		switch (n) {
		case UP: {
			if (y > 42) {
				gotoxy(x - 2, y);
				printf(" ");      //'>'을 지움
				gotoxy(x - 2, --y);
				printf(">");      //좌표((x - 2, y-1)에 '>'다시 출력
			}
			break;
		}

		case DOWN: {
			if (y < 44) {
				gotoxy(x - 2, y);
				printf(" ");      //'>'을 지움
				gotoxy(x - 2, ++y);
				printf(">");      //좌표((x - 2, y+1)에 '>'다시 출력
			}
			break;
		}

		case SUBMIT: {
			return y - 42;
		}
		}
	}



}